package Array;

public class Ex02 {

	public static void main(String[] args) {
		
		// index 범위: 0~6
		int[] arr = new int[7]; // 길이가 5인 int배열 arr 생성
		System.out.println("arr.length = " + arr.length);
		
		// 배열의 모든 요소 출력
		for(int i = 0; i <= arr.length; i++) {
			System.out.println("arr[" + i + "]: " + arr.length);
		}
	}
}
